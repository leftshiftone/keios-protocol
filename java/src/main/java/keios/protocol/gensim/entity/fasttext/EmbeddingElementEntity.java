package keios.protocol.gensim.entity.fasttext;

import java.util.Objects;

/**
 * @author Michael Mair
 */
public class EmbeddingElementEntity {

    private final float value;

    public EmbeddingElementEntity(float value) {
        this.value = value;
    }

    public float getValue() {
        return value;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        EmbeddingElementEntity that = (EmbeddingElementEntity) o;
        return Float.compare(that.value, value) == 0;
    }

    @Override
    public int hashCode() {
        return Objects.hash(value);
    }

    @Override
    public String toString() {
        return "EmbeddingElementEntity{" +
                "value=" + value +
                '}';
    }
}
